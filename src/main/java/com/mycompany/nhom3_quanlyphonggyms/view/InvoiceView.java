    public void showInvoices(List<Invoice> invoices) {
    DecimalFormatSymbols symbols = new DecimalFormatSymbols();
    symbols.setGroupingSeparator('.');
    symbols.setDecimalSeparator(',');  // nếu có phần thập phân, sẽ dùng dấu ','

    DecimalFormat df = new DecimalFormat("#,###", symbols);

    DefaultTableModel model = (DefaultTableModel) tblInvoice.getModel();
    model.setRowCount(0);

    for (Invoice inv : invoices) {
        String formattedFee = df.format(inv.getFee()) + " VND";

        model.addRow(new Object[]{
            inv.getMemberId(),
            inv.getMemberName(),
            inv.getExerciseType(),
            formattedFee
        });
    }
}
