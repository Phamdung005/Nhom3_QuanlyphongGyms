    private void initInvoiceTable() {
    String[] columnNames = { "Mã học viên", "Tên học viên", "Loại hình", "Học phí" };
    DefaultTableModel model = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
    tblInvoice.setModel(model);

    // Căn phải cột học phí
    DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
    rightRenderer.setHorizontalAlignment(SwingConstants.RIGHT);
    tblInvoice.getColumnModel().getColumn(3).setCellRenderer(rightRenderer);
}

