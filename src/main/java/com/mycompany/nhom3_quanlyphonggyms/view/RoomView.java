    public void showRoom(Room room) {
        txtId.setText(room.getId());
        txtName.setText(room.getName());
        txtCapacity.setText(String.valueOf(room.getCapacity()));
    }

    public void showRoomList(List<Room> list) {
        tableModel.setRowCount(0);
        for (Room r : list) {
            tableModel.addRow(new Object[]{r.getId(), r.getName(), r.getCapacity()});
        }
    }

    public void clearRoomInfo() {
        txtId.setText("");
        txtName.setText("");
        txtCapacity.setText("");
    }

